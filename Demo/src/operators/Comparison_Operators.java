package operators;

public class Comparison_Operators {
	
	public static void main (String []args) {
		
		int a= 30;
		int b=80;
		
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
	}

}
