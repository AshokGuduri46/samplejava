package operators;

public class Arithmetic {

	public static void main(String[] args) {

	int a = 10;
	int b =12;
	
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a%b);
	
	
	int c =6;
	c++;
	System.out.println(c);
	
	int d =9;
	d--;
	System.out.println(d);

	}

}
